from enum import Enum


class Nature(Enum):
    WATER = "Water"
    EARTH = "Earth"
    WIND  = "Wind"
    FIRE  = "Fire"


class Metals(Enum):
    COL    = "Col"
    GOLD   = "Gold"
    SILVER = "Silver"
    BRONZE = "Bronze"
    SELEN  = "Selen"


class Colors(Enum):
    COLORS = "Colors"
    RED    = "Red"
    GREEN  = "Green"
    BLUE   = "Blue"
    YELLOW = "Yellow"